package org.example.mvc.app;

public class Item {
    private long id;
    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String toString() {
        return "Item {id=" + this.id + ", name=" + this.name + "}";
    }
}
